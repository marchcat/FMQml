QVariantMap MockFileProviderPlugin::triggerAction(const QString &actionId, const FileActionContext &context)
{
    if (actionId == QStringLiteral("mock.item-info")) {
        QString normalizedPath = context.targetPath;
        normalizedPath.replace(QLatin1Char('\\'), QLatin1Char('/'));
        const QString targetName = normalizedPath.section(QLatin1Char('/'), -1);
        return {
            {QStringLiteral("title"), QStringLiteral("Mock Item Action")},
            {QStringLiteral("message"),
             QStringLiteral("Item: %1\nPath: %2\nCurrent folder: %3")
                 .arg(targetName.isEmpty() ? QStringLiteral("(unknown)") : targetName,
                      context.targetPath,
                      context.currentPath)},
        };
    }

    if (actionId == QStringLiteral("mock.folder-info")) {
        return {
            {QStringLiteral("title"), QStringLiteral("Mock Folder Action")},
            {QStringLiteral("message"),
             QStringLiteral("Current folder:\n%1").arg(context.currentPath)},
        };
    }

    return {
        {QStringLiteral("title"), QStringLiteral("Mock Action")},
        {QStringLiteral("message"), QStringLiteral("Unknown mock action.")},
    };
}
