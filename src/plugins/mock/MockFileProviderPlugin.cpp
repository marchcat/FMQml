struct MockNode {
    QString path;
    QString name;
    bool directory = false;
    QByteArray content;
};

QString normalizedMockPath(QString path)
{
    path = path.trimmed();
    const int separatorIndex = path.indexOf(QStringLiteral("://"));
    if (separatorIndex < 0) {
        const QString lower = path.toLower();
        if (lower == QStringLiteral("mock:") || lower == QStringLiteral("mock:/")) {
            return QStringLiteral("mock://");
        }
        return {};
    }

    const QString scheme = path.left(separatorIndex).toLower();
    if (scheme != QStringLiteral("mock")) {
        return {};
    }

    QString tail = path.mid(separatorIndex + 3);
    tail.replace(QLatin1Char('\\'), QLatin1Char('/'));
    while (tail.startsWith(QLatin1Char('/'))) {
        tail.remove(0, 1);
    }
    while (tail.endsWith(QLatin1Char('/'))) {
        tail.chop(1);
    }

    if (tail.isEmpty()) {
        return QStringLiteral("mock://");
    }

    const QStringList parts = tail.split(QLatin1Char('/'), Qt::SkipEmptyParts);
    return QStringLiteral("mock://") + parts.join(QLatin1Char('/'));
}

QString parentMockPath(const QString &path)
{
    const QString normalized = normalizedMockPath(path);
    if (normalized.isEmpty() || normalized == QStringLiteral("mock://")) {
        return {};
    }

    const QString tail = normalized.mid(QStringLiteral("mock://").size());
    const int slashIndex = tail.lastIndexOf(QLatin1Char('/'));
    if (slashIndex < 0) {
        return QStringLiteral("mock://");
    }
    return QStringLiteral("mock://") + tail.left(slashIndex);
}

QString fileNameForMockPath(const QString &path)
{
    const QString normalized = normalizedMockPath(path);
    if (normalized == QStringLiteral("mock://")) {
        return QStringLiteral("Mock");
    }
    const QString tail = normalized.mid(QStringLiteral("mock://").size());
    const int slashIndex = tail.lastIndexOf(QLatin1Char('/'));
    return slashIndex < 0 ? tail : tail.mid(slashIndex + 1);
}

QString childMockPath(const QString &parentPath, const QString &name)
{
    const QString normalizedParent = normalizedMockPath(parentPath);
    const QString cleanName = QString(name).trimmed().replace(QLatin1Char('\\'), QLatin1Char('/'));
    const QStringList parts = cleanName.split(QLatin1Char('/'), Qt::SkipEmptyParts);
    if (normalizedParent.isEmpty() || parts.isEmpty()) {
        return normalizedParent;
    }
    if (normalizedParent == QStringLiteral("mock://")) {
        return normalizedParent + parts.join(QLatin1Char('/'));
    }
    return normalizedParent + QLatin1Char('/') + parts.join(QLatin1Char('/'));
}

QString suffixForName(const QString &name)
{
    const int dotIndex = name.lastIndexOf(QLatin1Char('.'));
    if (dotIndex <= 0 || dotIndex == name.size() - 1) {
        return {};
    }
    return name.mid(dotIndex + 1).toLower();
}

QString byteSizeText(qint64 size)
{
    if (size < 1024) {
        return QStringLiteral("%1 B").arg(size);
    }
    const double kib = double(size) / 1024.0;
    if (kib < 1024.0) {
        return QStringLiteral("%1 KB").arg(kib, 0, 'f', 1);
    }
    return QStringLiteral("%1 MB").arg(kib / 1024.0, 0, 'f', 1);
}

QDateTime mockModifiedTime()
{
    return QDateTime::fromString(QStringLiteral("2026-01-01T12:00:00Z"), Qt::ISODate);
}

